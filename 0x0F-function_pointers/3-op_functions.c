#include "3-calc.h"

/**
 * op_add - makes the addition.
 * @a : 1st argument.
 * @b : 2nd argument.
 * Return: the sum of a and b.
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - makes the subtraction.
 * @a : 1st argument.
 * @b : 2nd argument.
 * Return: the subtract a and b.
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - makes the multiplication.
 * @a : 1st argument.
 * @b : 2nd argument.
 * Return: the multiplication of  a and b.
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - makes the divition.
 * @a : 1st argument.
 * @b : 2nd argument.
 * Return: the divition of  a and b.
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - makes the modulo.
 * @a : 1st argument.
 * @b : 2nd argument.
 * Return: the modulo of  a and b.
 */
int op_mod(int a, int b)
{
return (a % b);
}

